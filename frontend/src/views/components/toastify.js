import { toast } from "react-toastify";
import "react-toastify/dist/ReactToastify.css";

export function ToastDataUpdateSuccess() {
  toast.success("Data has update succesfully", {
    position: "top-right",
    autoClose: 3000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "light",
  });
}

export function ToastDataUpdateFailed(message) {
  toast.error(message || "Data has failed to update", {
    position: "top-right",
    autoClose: 3000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "light",
  });
}

